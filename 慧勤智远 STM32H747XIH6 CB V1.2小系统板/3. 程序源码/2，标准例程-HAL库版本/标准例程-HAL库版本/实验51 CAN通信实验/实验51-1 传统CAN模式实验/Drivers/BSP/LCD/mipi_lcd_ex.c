/**
 * @brief       3.5寸MIPI ST7796初始化代码
 * @param       无
 * @retval      0, 成功; 其他, 失败;
 */
uint8_t mipi_lcd_ex_st7796_cmdinit(void)
{
    uint8_t ret = 0;
    uint8_t reg_data[4] = {0};
    uint16_t i;
    uint16_t init_cmds_size = 0;
  
    ret += mipilcd_write_cmd(0, 0x11, (uint8_t *)reg_data, 0);   
    delay_ms(200);
    
    init_cmds_size = sizeof(st7796_init_cmd_tbl) / sizeof(mipi_lcd_init_cmd_t);
 
    /* 发送初始化序列 */
    for (i = 0; i < init_cmds_size; i++)
    {
         ret += mipilcd_write_cmd(0, st7796_init_cmd_tbl[i].cmd, (uint8_t *)st7796_init_cmd_tbl[i].data, st7796_init_cmd_tbl[i].data_bytes);    
    }
       
    delay_ms(200);
    ret += mipilcd_write_cmd(0, 0x21, (uint8_t *)reg_data, 0);
  
    ret += mipilcd_write_cmd(0, 0x29, (uint8_t *)reg_data, 0);   
    
    ret += mipilcd_write_cmd(0, 0x2C, (uint8_t *)reg_data, 0);  
    delay_ms(50);
    
    return ret;
}

